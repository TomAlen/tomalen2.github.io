package pojo.single2;

public class VipCard {
    private Integer id;
    private String no;
    private String level;
    private person myself;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
	public person getMyself() {
		return myself;
	}
	public void setMyself(person myself) {
		this.myself = myself;
	}
    
    
}
